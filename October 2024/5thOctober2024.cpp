class Solution {
  public:
    long long findSmallest(vector<int> &a) {
        int n=a.size();
        long long k=1;
        for(int i=0;i<n;i++){
            if(a[i]>k) return k;
            k=k+a[i];
        }
        
        return k;
    }
};

