// User function template for C++
class Solution {
  public:
    /* Returns count of pairs with difference k  */
    int countPairsWithDiffK(vector<int>& a, int k) {
        int n=a.size(),c=0;
        map<int,int> m;
        for(int i:a) m[i]++;
        for(int i=0;i<n;i++){
            if(m[ k + a[i]]) c=c+ m[k+a[i]];
        }
        return c;
    }
};

