class Solution {
  public:
    int pairWithMaxSum(vector<int>& a) {
       int n=a.size();
       if(n==1) return -1;
       int k=-1e5;
       for(int i=0;i<n-1;i++){
           k=max(k,a[i]+a[i+1]);
       }
       return k;
    }
};

