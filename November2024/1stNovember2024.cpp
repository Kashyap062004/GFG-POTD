class Solution {
  public:
    long long maxSum(vector<int>& a) {
       int n=a.size();
       sort (a.begin(),a.end());
       long long  s=0;
       int j= n-1;
       for (int i=0; i< n; i++){
          s=s+ abs (a[i] - a[j--]);
       }
       return s;
    }
};


